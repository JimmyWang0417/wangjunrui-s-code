#include <cmath>
#include <cstdio>
using namespace std;
int a[20000], n, t;
int main()
{
    scanf("%d", &n);
    for (register int i = 2, j; i <= n; i++)
    {
        int temp = sqrt(i);
        for (j = 2; j <= temp; j++)
            if (i % j == 0)
                break;
        if (j > temp)
            a[++t] = i;
    }
    for (register int i = 1; i <= t; i++)
        for (register int j = 1; j <= t; j++)
            for (register int k = 1; k <= t; k++)
                if (a[i] + a[j] + a[k] == n)
                {
                    printf("%d %d %d\n", a[i], a[j], a[k]);
                    return 0;
                }
}
