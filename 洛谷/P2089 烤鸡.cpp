#include <cstdio>
using namespace std;
int a[59049][11], temp[11], sum, ans, n;
void DFS(int t)
{
    for (register int i = 1; i <= 3; i++)
    {
        ans += i;
        temp[t] = i;
        if (t == 10)
        {
            if (ans == n)
            {
                sum++;
                for (register int j = 1; j <= 10; j++)
                    a[sum][j] = temp[j];
            }
        }
        else
            DFS(t + 1);
        ans -= i;
    }
}
int main()
{
    scanf("%d", &n);
    DFS(1);
    printf("%d\n", sum);
    for (register int i = 1; i <= sum; i++)
    {
        printf("%d", a[i][1]);
        for (register int j = 2; j <= 10; j++)
            printf(" %d", a[i][j]);
        putchar('\n');
    }
}
